            <p class="post-content">
            In the morning of October 9, 2017, the management team and staff of Phnom Penh International University (PPIU) met and introduced 05 awardees of SHARE scholarships Batch 3 from 2 different countries including Indonesia, Myanmar and Vietnam to study at Phnom Penh International University for 01 semester.
            </p>

            <p class="post-content">
            WHAT IS SHARE?
            </p>

            <p class="post-content">SHARE is an EU Grant funded project with an overarching objective to strengthen regional cooperation, enhance the quality, competitiveness and internationalization of ASEAN higher education institutions and students, contributing to an ASEAN Community beyond 2015. The main aim is to enhance cooperation between the EU and the ASEAN Economic Community (AEC) and to create lasting benefits from the harmonization of higher education across ASEAN.</p>
            
            <p class="post-content">“ASEAN Community is all about the future of young generation of ASEAN who will soon become the leaders and the workforce of this region. As such, we continuously work towards giving them opportunity to develop themselves, discover new knowledge, meet new friends and appreciate better their region. The SHARE scholarship is an excellent scheme that allows them to do just that…”, said Mr. Vongthep Arthakaivalvatee, Deputy Secretary-General for the ASEAN Socio-Cultural Community.</p>

            <p class="post-content"> The SHARE scholarship programme offers a fully-funded, one semester exchange for undergraduate students from selected universities across the ASEAN region.</p>

