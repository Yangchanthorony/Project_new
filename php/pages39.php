            <div style="text-align: center;" class="post-title">
            “PPIU’S QUALITY ASSURANCE SYSTEM IS ALIGNED WITH THE REQUIREMENTS OF THE ASEAN QUALITY ASSURANCE FRAMEWORK’S (AQAF) CRITERIA”, according to the panel of SHARE’s assessors
            </div>  

           <p>As a commitment to quality, PPIU’s Vision clearly states that <span  style="font-weight: bold;"> “PPIU is a University committed to local, regional (ASEAN) and international Standards.”</span></p>
           
            <p>Thus, we are committed to standards set out by the Cambodia’s high authority on quality, the ACC (Accreditation Committee of Cambodia).</p>

            <p>SHARE is a program by European Union and ASEAN to strengthen regional co-operation and enhance the quality, regional competitiveness and internationalization of ASEAN higher education institutions.</p>

            <p>On 5 and 6 February 2018, a panel of 4 SHARE’s assessors, specialized in quality assurance in ASEAN and Europe, came to make assessment of PPIU. The main purpose of the assessment is to evaluate the alignment of PPIU’s internal quality assurance (IQA) standards with the ASEAN Quality Assurance Framework (AQAF).   </p>

            <p>In June 2018, the 4 SHARE’s assessors, issued a report saying,</p>

            <h6 style="font-weight: bold;">  “In sum it can be stated that PPIU’s quality assurance system is aligned with the requirements of the ASEAN Quality Assurance Framework’s (AQAF) criteria”.</h6>

            <p>The 4 SHARE’s assessors also commend PPIU as follows:</p>

            <h6 style="font-weight: bold;"> “PPIU has initiated a process of placing high emphasis on quality and quality assurance procedures. The administrative and academic institutions which are in charge of the respective task are cooperating well and are creating benefit for students and other stakeholders.</h6>

            <h6 style="font-weight: bold;">  “… PPIU has developed an academic spirit that goes beyond being a service provider that simply delivers good learning conditions and degrees to the students.  Members of PPIU actively strive to create an academic spirit in which members of the university – from the Rector to students – feel as a community.”</h6>

            <p>They also made some recommendations, which PPIU will do its best to further strengthen its quality.</p>

