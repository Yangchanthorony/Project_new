            <h5 style=" font-weight: bold;"  class="post-content">
            WHAT IS SHARE?
            </h5>

            <p>SHARE is an EU Grant funded project with an overarching objective to strengthen regional cooperation, enhance the quality, competitiveness and internationalization of ASEAN higher education institutions and students, contributing to an ASEAN Community beyond 2015. The main aim is to enhance cooperation between the EU and the ASEAN Economic Community (AEC) and to create lasting benefits from the harmonization of higher education across ASEAN.</p>
           
            <p>“ASEAN Community is all about the future of young generation of ASEAN who will soon become the leaders and the workforce of this region. As such, we continuously work towards giving them opportunity to develop themselves, discover new knowledge, meet new friends and appreciate better their region. The SHARE scholarship is an excellent scheme that allows them to do just that…”, said Mr. Vongthep Arthakaivalvatee, Deputy Secretary-General for the ASEAN Socio-Cultural Community.</p>

            <p>The SHARE scholarship programme offers a fully-funded, one semester exchange for undergraduate students from selected universities across the ASEAN region.</p>

            <h5 style=" font-weight: bold;"  class="post-content">
            PPIU and SHARE
            </h5>

            <p> <span style=" font-weight: bold;">PPIU</span> is one <span style=" font-weight: bold;">among the four HEIs in Cambodia granted the opportunity to involve in SHARE Scholarship Program</span>. We have received and sent students under this scholarship scheme.</p>

           <p>As a result, <span style=" font-weight: bold;" >PPIU-SHARE awardees (PPIU-SHARE inbound students)</span> from <span  style=" font-weight: bold;">Indonesia</span> have successfully finished their <span  style=" font-weight: bold;">selected courses</span> and <span  style=" font-weight: bold;">passed exams</span> with high scores. Today, they received certificates and <span  style=" font-weight: bold;">academic transcripts </span>from Rector of PPIU, Madam <span  style=" font-weight: bold;">TEP Kolap</span>.</p>

           <p style=" font-weight: bold;" >Credits earned by SHARE awardees are transferable.</p>

           <p>SHARE awardees have not just learnt in class but also outside class; they have leant how to live in different country with different people and culture.</p>

           <p>Next semester, <span style=" font-weight: bold;">Oct</span>. 2017, PPIU will receive another batch of SHARE awardees (inbound) from different countries in ASEAN. Likewise, PPIU students (outbound) will also be sent to host universities at the same time.</p>
        
        
        
        
        
        
