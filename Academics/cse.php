    <p class="aa" style="margin-left: 40px;" id="bachelor-text-1">The Faculty of Science and Information Technology offers educational programs in Computer Science, Management Information Systems, Telecommunications, and Mathematics. Students who graduate from the Faculty of Science and Information Technology will be capable of managing and working in various fields, such as telecommunications companies, data management systems in all institutions, or creating their own businesses. The field of Information Technology has become a major factor in the development of society across all sectors, whether public or private institutions, and it is essential for these institutions to adopt information technology to advance their operations. In line with ASEAN's integration in 2015, Phnom Penh International University is committed to developing human resources through Computer Science and Information Technology programs that align with the evolving scientific and market demands. The university has recruited professors with knowledge and work experience in all areas to teach students, ensuring quality education both theoretically and practically.</p>

