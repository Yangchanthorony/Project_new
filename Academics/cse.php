    <p class="aa" style="margin-left: 60px;" id="objective-3">- Continuously improving the curriculum to meet market needs both locally and internationally.</p>

    <p class="aa" style="margin-left: 60px;" id="objective-4">- Providing students with new knowledge and experiences to meet both local and international market demands.</p>

