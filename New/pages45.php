            <p class="post-content">
            នាព្រឹកថ្ងៃទី 15 ខែ កញ្ញា ឆ្នាំ 2017 នៅសាកលវិទ្យាល័យភ្នំពេញអន្តរជាតិ (PPIU) 
            បានរៀបចំសន្និសីទសារព័ត៌មាននៃការបើកបណ្តុះបណ្តាលជាផ្លូវការជំនាញថ្មី “ទំនាក់ទំនងអន្តរជាតិ - 
            International Relations” ក្រោមអធិបតីភាពលោកស្រី <span style="font-weight: bold;">ទេព កុលាប </span>សាកលវិទ្យាធិការនៃសាកលវិទ្យាល័យភ្នំពេញអន្តរជាតិ (PPIU),<span style="font-weight: bold;"> ឯកឧត្តម អ៊ុង ស៊ាន</span> រដ្ឋលេខាធិការ នៃក្រសួងការបរទេស
            </p>

            <p class="post-content">
            និងសហប្រតិបត្តិការអន្តរជាតិ, <span  style="font-weight: bold;">ឯកឧត្តម ម៉ក់ ងយ</span> អគ្គនាយក នៃអគ្គនាយកដ្ឋានឧត្តមសិក្សា និង  <span style="font-weight: bold;"> ឯកឧត្តម ណាត់តាវុធ ផូទីសារ៉ូ</span>
             ឯកអគ្គរាជទូតនៃព្រះរាជាណាចក្រថៃប្រចាំព្រះរាជាណាចក្រ      កម្ពុជា ។
            </p>

            <p class="post-content"> នៅក្នុងសន្និសីទសារព័ត៌មាននេះដែរ លោកស្រីសាកលវិទ្យាធិការបានគូសបញ្ជាក់ពីសារៈប្រយោជន៍នៃការសិក្សាជំនាញ “ទំនាក់ទំនងអន្តរជាតិ - International Relations” ដែលត្រូវរៀបចំឡើងស្របតាមគុណវុឌិ៍្ឍជាតិ ដើម្បីផ្តល់នូវចំណេះដឹងប្រកបដោយគុណភាព និង គុណធម៌សម្រាប់បំពេញបន្ថែមនូវធនធានមនុស្សដែលកំពុងខ្វះខាតទាំងស្ថាប័នសាធារណៈ ក៏ដូចជាស្ថាប័ន ឯកជន អង្គការជាតិ និង អន្តរជាតិ ។ ជាងនេះទៅទៀត “ទំនាក់ទំនងអន្តរជាតិ - International Relations” និងផ្តោតសំខាន់ទៅលើ ទំនាក់ទំនងរវាងរដ្ឋ​និង រដ្ឋ, ទំនាក់ទំនងរដ្ឋ និង អង្គការអន្តរជាតិ, ទំនាក់ទំនងរវាងអង្គការអន្តរជាតិ និង អង្គការអន្តរជាតិ ។</p>
            
            <p class="post-content">    ក្រោយបញ្ចប់ការសិក្សាលើជំនាញនេះចប់ជាស្ថាពរ អ្នកសិក្សានិងទទួលបាននូវការយល់ដឹងយ៉ាងទូលំទូលាយពីប្រវត្តិសាស្រ្តពិភពលោក សហស្សសម័យ និង ការអភិវឌ្ឍសង្គម, ការយល់ដឹងពីភូមិសាស្រ្តនយោបាយ សេដ្ឋកិច្ចនយោបាយ និងច្បាប់អន្តរជាតិ, ចំណេះដឹងកិច្ចការទូត និងការវិភាគកិច្ចការបរទេស និង ជំនាញនៃឥរិយាបថក្នុងការប្រាស្រ័យទាក់ទងទាំងកិច្ចការធុរកិច្ច និង នយោបាយជាដើម ។</p>

            <p class="post-content">
            លោកស្រី <span style="font-weight: bold;">ទេព កុលាប</span> សាកលវិទ្យាធិការនៃសាកលវិទ្យាល័យភ្នំពេញអន្តរជាតិ (PPIU) បានលើកទឹកចិត្តដល់សិស្សានុសិស្សអោយចូលសិក្សានៅមុខជំនាញនេះព្រោះថា IR និងផ្តល់ឱកាសដល់អ្នកសិក្សាមានសមត្ថភាពគ្រប់គ្រាន់ដើម្បីបំពេញកិច្ចការក្នុងស្ថាប័នសាធារណៈ និង ឯកជូនដូចជា ៖ កិច្ចការប្រាស្រ័យទាក់ទងសាធារណៈ, អ្នកការទូត, កុងស៊ុល, សេនានុព័ន្ធ(កិច្ចសហប្រត្តិបត្តិការផ្នែកយោធា), អ្នកវិភាគនយោបាយ និង សេដ្ឋកិច្ច, អ្នកជំនាញខាងគោលនយោបាយ, អ្នកជំនាញសារជីវកម្មអន្តរជាតិ, អ្នកជំនាញខាងផ្នែកសិទ្ធមនុស្ស, អ្នកធ្វើកិច្ចការក្នុងសង្គមស៊ីវិល, អ្នកស្រាវជ្រាវ និង គ្រូបង្រៀននៅគ្រឹះស្ថានឧត្តមសិក្សា… ៕
            </p>

            


