            <h6  style="text-align: center; font-weight: bold; margin-top: 10px;"> CALL FOR APPLICATION</h6>
            <p style="text-align: center;">  DIES National Multiplication Trainings 2019-2020</p>

            <p style=" font-weight: bold;">1. Background</p>

            <p>Dialogue on Innovative Higher Education Strategies, known as DIES, jointly coordinated by the German Academic Exchange Service (DAAD) and German Rectors’ Conference (HRK) and financed by German Federal Ministry for Economic Development and Cooperation (BMZ), has been offering dialogue seminars, conferences and trainings for higher education managers in developing countries for more than a decade. Currently DIES has more than 8,000 alumni who took part in its different activities.</p>
           
            <p>With the aim to strengthen DIES alumni in their roles as multipliers in higher education management and to promote the sustainability of the DIES training courses, DIES has been supporting National Multiplication Trainings (NMT) since 2013. The last NMT cohort, which was centrally coordinated by the Center for Quality Development (ZfQ) at the University of Potsdam in Germany, was successfully completed in 2018 with ten alumni teams implementing trainings in Africa, Latin America and Southeast Asia with over 200 participants in total.</p>
       
            <p>In the 2019-2020 cohort, CamEd Institute in collaboration with Phnom Penh International University (PPIU) have been provided technical support and granted funding of 37,035.10EUR, including 20,941EUR from DIES-National Multiplication Trainings (NMT) program and 16,094.10EUR in-cash and in-kind contributions from CamEd and PPIU. With these financial and technical support, CamEd and PPIU are organizing a National Multiplication Training Program known as DIES NMT-Cambodia 2019-2020. The main purpose of this program is to organize two 3-day training workshops to develop the capacity of higher education management, particularly deans and vice deans throughout the country.</p>
            
            <p style=" font-weight: bold;">2. The Objectives</p>

            <p>NMT-Cambodia 2019-2020 aims to achieve the following objectives:</p>

            <p>1. To develop a full understanding of the multifunctional role of academic deans in local, regional and international higher education contexts</p>
           
            <p>2. To identify common and specific challenges that academic deans face in the Cambodian higher education context</p>
            
            <p>3. To equip the participants with knowledge and skills for managing their faculty effectively and efficiently</p>
           
            <p style=" font-weight: bold;">3. Learning Outcomes</p>

            <p>At the end of the DIES NMT-Cambodia 2019-2020 program, the participants will be able to perform the following task:</p>

            <p>1. Have in-depth understanding of higher education management concepts, theories and practices</p>

            <p>2. Compare and contrast roles of academic deans in local, regional, and international higher education contexts</p>

            <p>3. Discuss and draw implications of issues and challenges faced by academic deans</p>

            <p>4. Develop a realistic personal action plan (PAP) and implement it effectively to assure the quality of their faculties/colleges</p>

            <p>5. Build professional networks for future collaborations with local and international institutions</p>

            <p style=" font-weight: bold;"> 4. Eligible Applicants</p>

            <p>To apply for the DIES NMT-Cambodia 2019-2020 Program, the applicants need to fulfil all of the following requirements:</p>

            <p>+ Currently a dean or vice dean of a higher education institution (HEI) in Cambodia</p>

            <p>+ At least Master’s degree</p>

            <p>+ At least 3-year experience as dean or vice dean in Cambodian HEIs</p>

            <p>+ English proficiency: TOEFL, IELTS, or certification from their supervisor</p>

            <p>+ Letter of Permission from the current institution’s rector or president</p>

            <p>+ Personal Letter of Motivation</p>

            <p>+ Letter of Confirmation of Participation to ensure the applicant’s full commitment to complete the program and make contributions to their current institution</p>

            <p style=" font-weight: bold;"> 5. DIES NMT-Cambodia 2019-2020 Framework</p>

            <p>This training program will be composed of four phases, including preliminary phase, training workshop 1 phase, training workshop 2 phase, and follow-up phase. During the preliminary phase, the selected participants will be required to do some reading of the relevant materials to be provided by the trainers and answer some comprehension questions before they share their outputs through webinar discussion, Skype and other available social media.</p>
    
            <p>This knowledge and tentative topic will be discussed in details with the trainers and experts during the first workshop which will last for 3 days and will cover educational management topics</p>

            <p>The second workshop which will last for 3 days and will focus on practical aspects of the program. It includes action planning, decision making, conflict management, and PAP presentation. At the end of this part is the presentation of the PAP results by the participants.</p>

            <p>The fourth phase will be webinar and other social media when all the participants virtually interact with each other to share updates or progress of their work that is perceived as impact of the program, and they will use this opportunity to do self-reflection and build long-time networking for future collaboration.</p>

